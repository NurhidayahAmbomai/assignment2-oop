    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        String name=jTextField13.getText().toString();
        String total=jTextField4.getText().toString();
        String grade=jTextField6.getText().toString();
        String avg=jTextField5.getText().toString();

        try{
            FileWriter writer = new FileWriter("student List.txt",true);
            
            writer.write(name);
            writer.write(" ");
            writer.write(total);
            writer.write(" ");
            writer.write(grade);
            writer.write(" ");
            writer.write(avg);

            writer.write(System.getProperty("line.separator"));
            
            writer.close();
            
            JOptionPane.showMessageDialog(rootPane,"Success");

        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane,"Error");
        }
    }//GEN-LAST:event_jButton10ActionPerformed

