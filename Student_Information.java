    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        DefaultTableModel model=(DefaultTableModel) jTable1.getModel();

        if(jTable1.getSelectedRow()==-1){
            if(jTable1.getSelectedRow()==0){
                JOptionPane.showMessageDialog(null,"Table is empty");
            }else{
                JOptionPane.showMessageDialog(null,"You must select which row to be updated");
            }
        }
        model.setValueAt(jTextField13.getText(), jTable1.getSelectedRow(), 0);
        
        model.setValueAt(jTextField4.getText(), jTable1.getSelectedRow(), 1);
        model.setValueAt(jTextField5.getText(), jTable1.getSelectedRow(), 2);
        model.setValueAt(jTextField6.getText(), jTable1.getSelectedRow(), 3);
    }//GEN-LAST:event_jButton5ActionPerformed

